// Offset/length slot base values, computed as cumulative sums of 1<<extraBits.
// Each table has one extra sentinel entry to aid the binary search in getSlot.
var (
	offsetSlotBase [maxNumOffsetSyms + 1]uint32
	lengthSlotBase [numLengthSyms + 1]uint32
)

func init() {
	base := uint32(1)
	for i, eb := range extraOffsetBits {
		offsetSlotBase[i] = base
		base += 1 << eb
	}
	offsetSlotBase[maxNumOffsetSyms] = base

	base = 1
	for i, eb := range extraLengthBits {
		lengthSlotBase[i] = base
		base += 1 << eb
	}
	lengthSlotBase[numLengthSyms] = base
}

// getSlot returns the slot whose base value range contains v (binary search).
func getSlot(v uint32, base []uint32, numSlots int) int {
	l, r := 0, numSlots-1
	for {
		slot := (l + r) / 2
		if v >= base[slot] {
			if v < base[slot+1] {
				return slot
			}
			l = slot + 1
		} else {
			r = slot - 1
		}
	}
}

// getNumOffsetSlots returns the number of offset slots needed for a buffer of
// the given uncompressed size.
func getNumOffsetSlots(uncompressedSize int) int {
	if uncompressedSize < 2 {
		return 0
	}
	return 1 + getSlot(uint32(uncompressedSize-1), offsetSlotBase[:], maxNumOffsetSyms)
}
